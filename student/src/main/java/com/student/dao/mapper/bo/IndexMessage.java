package com.student.dao.mapper.bo;

public class IndexMessage {
    private String infoName;

    private Integer infoNum;

    private Float infoProp;

    private Integer indoType;

    public String getInfoName() {
        return infoName;
    }

    public void setInfoName(String infoName) {
        this.infoName = infoName;
    }

    public Integer getInfoNum() {
        return infoNum;
    }

    public void setInfoNum(Integer infoNum) {
        this.infoNum = infoNum;
    }

    public Float getInfoProp() {
        return infoProp;
    }

    public void setInfoProp(Float infoProp) {
        this.infoProp = infoProp;
    }

    public Integer getIndoType() {
        return indoType;
    }

    public void setIndoType(Integer indoType) {
        this.indoType = indoType;
    }
}